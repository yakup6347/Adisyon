    private void loadItem() {

        itemlist = data.getOrderItems(localID);
        tableSetModel();
        page2MainScrollList.removeAll();

        for (int i = 0; i < itemlist.length; i++) {

            itemJPanel items = new itemJPanel(itemTable, hesap, itemlist[i].id, itemlist[i].name, itemlist[i].payed, itemlist[i].quantity, itemlist[i].price, payitemlist);

            if (i % 2 == 0) {
                items.setBackground(Color.DARK_GRAY);
            } else {
                items.setBackground(Color.GRAY);

            }

            page2MainScrollList.add(items);

        }
        page2MainScrollList.revalidate();
        page2MainScrollList.repaint();

    }

