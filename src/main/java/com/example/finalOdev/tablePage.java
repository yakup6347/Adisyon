    public void loadTables() {

        page1ScrollGrid.removeAll();
        page1ScrollGrid.setLayout(new GridLayout());

        
        tableProduct[] table = data.getTables();
        for (int j = 0; j < table.length; j++) {
            adisyon adisyon = new adisyon(table[j].id,data);
            tableJPanel tables = new tableJPanel(table[j].name, table[j].free, table[j].price);
            tables.setSize(new Dimension(150, 150));
            tables.addMouseListener(new MouseListener() {

                @Override
                public void mouseClicked(MouseEvent e) {
                    menu.removeAll();
                    menu.add(adisyon);
                    menu.revalidate();
                    menu.repaint();
                }

                @Override
                public void mousePressed(MouseEvent e) {

                }

                @Override
                public void mouseReleased(MouseEvent e) {

                }

                @Override
                public void mouseEntered(MouseEvent e) {

                }

                @Override
                public void mouseExited(MouseEvent e) {

                }
            });
            
            page1ScrollGrid.add(tables);
        }
        page1ScrollGrid.add(new tableAddJPanel(this,data));

        page1ScrollGrid.revalidate();
        page1ScrollGrid.repaint();

    }

    public tablePage(javax.swing.JLayeredPane menu, DataBase data) {
        this.data = data;
        this.menu = menu;
        initComponents();
        loadTables();
        updateLayout();
    }

