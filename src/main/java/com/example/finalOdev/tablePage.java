    public void updateLayout() {
        int width = page1Scroll.getWidth();

        for (int i = page1ScrollGrid.getComponentCount(); i < (page1Scroll.getWidth() / 160) * (page1Scroll.getHeight() / 160); i++) {

            JPanel panel = new JPanel();
            panel.setVisible(false);
            panel.setPreferredSize(new Dimension(150, 150));
            page1ScrollGrid.add(panel);
        }

        int columns = Math.max(1, width / (170));
        page1ScrollGrid.setLayout(new GridLayout(0, columns, 10, 10));
        page1ScrollGrid.revalidate();
    }

