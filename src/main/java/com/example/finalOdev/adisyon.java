    private void selectallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectallActionPerformed
        // TODO add your handling code here:
        tableSetModel();
        Object[] row = new Object[3];
        for (itemProduct item : itemlist) {
            boolean found = false;
            if (item.quantity > item.payed) {
                itemProduct newItem = new itemProduct();
                newItem.id = item.id;
                newItem.payed = item.quantity;
                payitemlist.add(newItem);
                

                for (int j = 0; j < itemTable.getModel().getRowCount(); j++) {

                    if (item.name.equals(itemTable.getModel().getValueAt(j, 0))) {
                      
                        int currentCount = (int) itemTable.getModel().getValueAt(j, 1);
                        itemTable.getModel().setValueAt(currentCount + item.quantity - item.payed, j, 1);

                        found = true;
                    }
                }
                if (!found) {
                    row[0] = item.name;
                    row[1] = item.quantity - item.payed;
                    row[2] = item.price;

                    model.addRow(row);
                    itemTable.setModel(model);
                }
                float sum = 0;
                for (int j = 0; j < itemTable.getModel().getRowCount(); j++) {
                    sum += (int) itemTable.getModel().getValueAt(j, 1) * (float) itemTable.getModel().getValueAt(j, 2);

                }
                hesap.setText(String.valueOf(sum));
            }
        }


    }//GEN-LAST:event_selectallActionPerformed

